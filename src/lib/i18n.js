export function getDefault(key) {
  return T[key] || { en: key, mr: key };
}

export function tr(key, isMr) {
  const o = OVERRIDES[key];
  if (o) {
    const v = isMr ? (o.mr ?? o.en) : (o.en ?? o.mr);
    if (v !== undefined && v !== null && v !== '') return v;
  }
  const e = T[key];
  if (!e) return key;
  return isMr ? e.mr : e.en;
}
